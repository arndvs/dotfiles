#!/usr/bin/env bash
# detect-client.sh — Detect active client and project from current directory.

if [[ "${BASH_SOURCE[0]}" != "$0" ]]; then
    _dc_client_oldopts=$(set +o)
    trap 'eval "$_dc_client_oldopts"; unset _dc_client_oldopts' RETURN
fi
set -euo pipefail

DOTFILES="${DOTFILES:-$HOME/dotfiles}"
CLIENTS_DIR="$DOTFILES/clients"
WORKING_DIR="$DOTFILES/working"
OUTPUT_FILE="$WORKING_DIR/active-client.md"

ACTIVE_CLIENT=""
ACTIVE_PROJECT=""

_expand_path() {
    local p="$1"
    p="${p/#\~\//$HOME/}"
    p="${p/#\~/$HOME}"
    echo "$p"
}

_write_empty_output() {
    mkdir -p "$WORKING_DIR"
    cat > "$OUTPUT_FILE" << 'EOF'
<!-- Auto-generated by detect-client.sh — no client context active -->
<!-- This file is regenerated on every cd() — do not edit manually -->
EOF
}

_write_active_output() {
    local client="$1"
    local project="$2"
    local client_dir="$CLIENTS_DIR/$client"
    local project_dir="$client_dir/projects/$project"
    local ts
    ts=$(date '+%Y-%m-%d %H:%M' 2>/dev/null || echo "")

    mkdir -p "$WORKING_DIR"

    {
        printf '<!-- Auto-generated by detect-client.sh — do not edit manually -->\n'
        printf '<!-- Client: %s | Project: %s | Updated: %s -->\n\n' "$client" "${project:-none}" "$ts"

        if [[ -f "$client_dir/client.instructions.md" ]]; then
            printf '## Active Client\n\n'
            printf '@%s/clients/%s/client.instructions.md\n\n' "$DOTFILES" "$client"
        fi

        if [[ -n "$project" ]] && [[ -f "$project_dir/project.instructions.md" ]]; then
            printf '## Active Project\n\n'
            printf '@%s/clients/%s/projects/%s/project.instructions.md\n\n' "$DOTFILES" "$client" "$project"
        fi
    } > "$OUTPUT_FILE"
}

_detect_client_project() {
    local cwd="$PWD"

    for client_dir in "$CLIENTS_DIR"/*/; do
        [[ -d "$client_dir" ]] || continue

        local client
        client=$(basename "$client_dir")
        [[ "$client" == "_template" ]] && continue

        local projects_file="$client_dir/.projects"
        [[ -f "$projects_file" ]] || continue

        while IFS= read -r line; do
            [[ -z "$line" ]] && continue
            [[ "$line" =~ ^[[:space:]]*# ]] && continue

            local mapped_path project_slug
            if [[ "$line" =~ = ]]; then
                mapped_path=$(echo "$line" | cut -d= -f1 | sed 's/[[:space:]]*$//')
                project_slug=$(echo "$line" | cut -d= -f2 | sed 's/^[[:space:]]*//' | sed 's/[[:space:]]*$//')
            else
                mapped_path=$(echo "$line" | sed 's/[[:space:]]*$//')
                project_slug=""
            fi

            mapped_path=$(_expand_path "$mapped_path")

            if [[ "$cwd" == "$mapped_path" ]] || [[ "$cwd" == "$mapped_path"/* ]]; then
                ACTIVE_CLIENT="$client"
                ACTIVE_PROJECT="$project_slug"
                return 0
            fi
        done < "$projects_file"
    done

    return 1
}

if [[ ! -d "$CLIENTS_DIR" ]]; then
    export ACTIVE_CLIENT=""
    export ACTIVE_PROJECT=""
    _write_empty_output
    return 0 2>/dev/null || exit 0
fi

if _detect_client_project; then
    export ACTIVE_CLIENT
    export ACTIVE_PROJECT
    _write_active_output "$ACTIVE_CLIENT" "$ACTIVE_PROJECT"

    # Inject cmd venture context if available
    _dc_cmd_script="$DOTFILES/bin/detect-cmd.sh"
    if [[ -f "$_dc_cmd_script" ]]; then
        source "$_dc_cmd_script" "$ACTIVE_CLIENT" 2>/dev/null || true
    fi
    unset _dc_cmd_script

    # HUD event — client context change (inline, non-blocking)
    {
        _dc_pipe="${DOTFILES}/working/hud.pipe"
        _dc_ts=$(date -u +"%Y-%m-%dT%H:%M:%SZ" 2>/dev/null || echo "")
        _dc_td=$(date +"%H:%M:%S" 2>/dev/null || echo "")
        _dc_msg="Client: ${ACTIVE_CLIENT}"
        [[ -n "$ACTIVE_PROJECT" ]] && _dc_msg="$_dc_msg | Project: $ACTIVE_PROJECT"
        _dc_ctx="${ACTIVE_CONTEXTS:-general}"
        _dc_payload=$(printf '{"type":"context","project":"%s","projectPath":"%s","contexts":"%s","message":"%s","timestamp":"%s","time":"%s"}' \
            "$(basename "$(pwd)" 2>/dev/null || echo "unknown")" "${PWD/$HOME/~}" "$_dc_ctx" "$_dc_msg" "$_dc_ts" "$_dc_td")
        if [[ -p "$_dc_pipe" ]] && [[ "$(uname -o 2>/dev/null)" != "Msys" ]]; then
            ( printf '%s\n' "$_dc_payload" > "$_dc_pipe" ) 2>/dev/null &
            disown 2>/dev/null
        else
            printf '%s\n' "$_dc_payload" >> "${DOTFILES}/working/events.jsonl" 2>/dev/null &
            disown 2>/dev/null
        fi
        unset _dc_pipe _dc_ts _dc_td _dc_msg _dc_ctx _dc_payload
    } 2>/dev/null || true
else
    export ACTIVE_CLIENT=""
    export ACTIVE_PROJECT=""
    _write_empty_output
fi

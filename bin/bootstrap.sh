#!/usr/bin/env bash
# bootstrap.sh — Set up ~/dotfiles on a fresh machine.
#
# Usage:
#   cd ~/dotfiles && bash bin/bootstrap.sh
#
# Idempotent — safe to re-run. Skips steps that are already done.
# Works on: Windows (Git Bash), Linux (VPS), macOS.

set -euo pipefail

DOTFILES="$HOME/dotfiles"
CLAUDE_DIR="$HOME/.claude"
COPILOT_DIR="$HOME/.copilot"
AGENTS_DIR="$HOME/.agents"
SECRETS_DIR="$DOTFILES/secrets"
VENV_DIR="$SECRETS_DIR/.venv"
CLIENTS_DIR="$DOTFILES/clients"

source "$(dirname "${BASH_SOURCE[0]}")/_lib.sh"

ADOPT_MODE=false
MINIMAL_MODE=false
LEGACY_CHECK_MODE=false
LEGACY_FORCE_MODE=false

for arg in "$@"; do
    case "$arg" in
        --adopt)
            ADOPT_MODE=true
            ;;
        --minimal)
            MINIMAL_MODE=true
            ;;
        --check)
            LEGACY_CHECK_MODE=true
            ;;
        --force)
            LEGACY_FORCE_MODE=true
            ;;
        --help|-h)
            echo "Usage: bash ~/dotfiles/bin/bootstrap.sh [--adopt] [--minimal] [--check] [--force]"
            echo ""
            echo "  (no flags)   Standard setup (idempotent)."
            echo "  --adopt      Merge an existing Claude setup into ctrl+shft with prompts."
            echo "  --minimal    Symlink-focused setup; skips secrets, shell wiring, venv, and supply-chain settings."
            echo "  --check      Legacy compatibility mode; runs validation only and exits."
            echo "  --force      Legacy compatibility alias (no-op; bootstrap already reconciles stale links)."
            exit 0
            ;;
        *)
            red "Unknown argument: $arg"
            red "Run: bash ~/dotfiles/bin/bootstrap.sh --help"
            exit 1
            ;;
    esac
done

if [[ "$LEGACY_CHECK_MODE" == true ]]; then
    yellow "Legacy flag --check detected (compatibility mode)."
    yellow "Running validation only (no bootstrap mutations)."
    _check_fail=0
    if bash "$HOME/dotfiles/bin/validate-env.sh"; then
        :
    else
        _check_fail=1
    fi
    if bash "$HOME/dotfiles/bin/validate-symlinks.sh"; then
        :
    else
        _check_fail=1
    fi
    exit $_check_fail
fi

# ── 1.5. Client scope directory + active context placeholder ─────────────────
echo
green "[1.5/13] Client scope scaffolding"
mkdir -p "$CLIENTS_DIR"
mkdir -p "$DOTFILES/working"
if [[ ! -f "$DOTFILES/working/active-client.md" ]]; then
    cat > "$DOTFILES/working/active-client.md" << 'ACTIVECLIENTEOF'
<!-- Auto-generated by detect-client.sh — no client context active -->
<!-- This file is regenerated on every cd() — do not edit manually -->
ACTIVECLIENTEOF
    green "  Created working/active-client.md placeholder"
else
    yellow "  working/active-client.md already exists — skipping"
fi

if [[ "$LEGACY_FORCE_MODE" == true ]]; then
    yellow "Legacy flag --force detected (compatibility no-op)."
    yellow "bootstrap.sh is idempotent and already repairs stale symlinks/fallback copies."
fi

# ── OS detection ──────────────────────────────────────────────────────────────
OS=$(detect_os)
green "Detected OS: $OS"

# Track failures across all steps
_fail=0

# ── Verify we're inside the dotfiles repo ─────────────────────────────────────
if [[ ! -f "$DOTFILES/CLAUDE.base.md" ]]; then
    red "Error: $DOTFILES/CLAUDE.base.md not found."
    red "Clone the repo first: git clone <repo-url> ~/dotfiles"
    exit 1
fi

if [[ "$ADOPT_MODE" == true ]]; then
    source "$(dirname "${BASH_SOURCE[0]}")/_adopt.sh"
    run_adopt
fi

# ── 1. Create secrets directory and split env files ───────────────────────────
echo
green "[1/13] Secrets directory"
if [[ "$MINIMAL_MODE" == true ]]; then
    yellow "  Skipping secrets setup (--minimal mode)"
else
    mkdir -p "$SECRETS_DIR"

# Migration hint for legacy .env users (must check BEFORE creating new files)
if [[ -f "$SECRETS_DIR/.env" ]] && [[ ! -f "$SECRETS_DIR/.env.agent" ]]; then
    yellow "  MIGRATION: legacy secrets/.env detected. Split into .env.agent + .env.secrets"
    yellow "  See templates: .env.agent.example and .env.secrets.example"
    yellow "  After migrating, delete secrets/.env to complete the transition"
fi

# .env.agent (non-sensitive config — sourced into shell)
if [[ -f "$SECRETS_DIR/.env.agent" ]]; then
    yellow "  secrets/.env.agent already exists — skipping"
else
    if [[ ! -f "$DOTFILES/.env.agent.example" ]]; then
        red "  Error: $DOTFILES/.env.agent.example not found"
        exit 1
    fi
    cp "$DOTFILES/.env.agent.example" "$SECRETS_DIR/.env.agent"
    green "  Created secrets/.env.agent from .env.agent.example"
    yellow "  >>> Fill in non-sensitive config: $SECRETS_DIR/.env.agent"
fi

# .env.secrets (sensitive credentials — process-scoped only)
if [[ -f "$SECRETS_DIR/.env.secrets" ]]; then
    yellow "  secrets/.env.secrets already exists — skipping"
else
    if [[ ! -f "$DOTFILES/.env.secrets.example" ]]; then
        red "  Error: $DOTFILES/.env.secrets.example not found"
        exit 1
    fi
    cp "$DOTFILES/.env.secrets.example" "$SECRETS_DIR/.env.secrets"
    green "  Created secrets/.env.secrets from .env.secrets.example"
    yellow "  >>> Fill in API keys and tokens: $SECRETS_DIR/.env.secrets"
fi
fi

# ── 2. Generate and link CLAUDE.md ─────────────────────────────────────────────
echo
green "[2/13] CLAUDE.md (generated from CLAUDE.base.md + local instructions)"
mkdir -p "$CLAUDE_DIR"
mkdir -p "$DOTFILES/skills/_local"
mkdir -p "$DOTFILES/instructions/_local"

# Generate CLAUDE.md from base + local instruction references
# Write the generated-file header, then append everything after the base header
{
    printf '<!-- GENERATED FILE — do not edit directly.\n'
    printf '     Built by bootstrap.sh from CLAUDE.base.md + local instruction refs.\n'
    printf '     Edit CLAUDE.base.md instead, then run: bash ~/dotfiles/bin/bootstrap.sh -->\n'
    # Skip the original HTML comment block, keep everything after -->
    awk 'BEGIN{skip=1} /-->/{skip=0; next} !skip{print}' "$DOTFILES/CLAUDE.base.md"
} > "$DOTFILES/CLAUDE.md"

_local_instructions=()
if [[ -d "$DOTFILES/instructions/_local" ]]; then
    for f in "$DOTFILES/instructions/_local/"*.instructions.md; do
        [[ -f "$f" ]] && _local_instructions+=("$f")
    done
fi
if [[ ${#_local_instructions[@]} -gt 0 ]]; then
    printf '\n## Local Instructions (auto-generated by bootstrap.sh)\n\n' >> "$DOTFILES/CLAUDE.md"
    for f in "${_local_instructions[@]}"; do
        _fname=$(basename "$f")
        printf '@~/dotfiles/instructions/_local/%s\n' "$_fname" >> "$DOTFILES/CLAUDE.md"
    done
    green "  Generated CLAUDE.md with ${#_local_instructions[@]} local instruction(s)"
else
    green "  Generated CLAUDE.md (no local instructions found)"
fi

# Link or copy to ~/.claude/
if [[ "${ADOPT_SKIP_CLAUDE_MD:-false}" == true ]]; then
    yellow "  Skipping ~/.claude/CLAUDE.md link (--adopt skip)"
else
    if [[ -L "$CLAUDE_DIR/CLAUDE.md" ]]; then
        _target=$(readlink "$CLAUDE_DIR/CLAUDE.md")
        if [[ "$_target" == "$DOTFILES/CLAUDE.md" ]]; then
            yellow "  ~/.claude/CLAUDE.md is already symlinked correctly"
        else
            ln -sf "$DOTFILES/CLAUDE.md" "$CLAUDE_DIR/CLAUDE.md"
            green "  Fixed stale symlink (was $_target)"
        fi
    elif [[ "$OS" == "windows" ]]; then
        cp "$DOTFILES/CLAUDE.md" "$CLAUDE_DIR/CLAUDE.md"
        yellow "  Copied CLAUDE.md to ~/.claude/ (Windows: file symlinks require admin)"
    else
        [[ -f "$CLAUDE_DIR/CLAUDE.md" ]] && yellow "  Replacing regular file with symlink"
        ln -sf "$DOTFILES/CLAUDE.md" "$CLAUDE_DIR/CLAUDE.md"
        green "  Symlinked ~/.claude/CLAUDE.md -> ~/dotfiles/CLAUDE.md"
    fi
fi

# ── 3. Symlink ~/.claude/skills/ ──────────────────────────────────────────────
echo
green "[3/13] Skills directory (Claude Code)"
if [[ "${ADOPT_SKIP_SKILLS:-false}" == true ]]; then
    yellow "  Skipping ~/.claude/skills link (--adopt skip)"
else
    ensure_symlink "$DOTFILES/skills" "$CLAUDE_DIR/skills" "~/.claude/skills"
fi

# Report discovered skills
_shared_skills=0
_local_skills=0
for d in "$DOTFILES/skills/"*/; do
    [[ -d "$d" ]] || continue
    _name=$(basename "$d")
    [[ "$_name" == "_local" || "$_name" == "_vendor" ]] && continue
    _shared_skills=$(( _shared_skills + 1 ))
done
if [[ -d "$DOTFILES/skills/_vendor" ]]; then
    yellow "  \"_vendor/ detected. Per ADR-001, move contents to skills/_local/vendor/ then delete skills/_vendor/.\""
fi
if [[ -d "$DOTFILES/skills/_local" ]]; then
    for d in "$DOTFILES/skills/_local/"*/; do
        [[ -d "$d" ]] && [[ -f "$d/SKILL.md" ]] && _local_skills=$(( _local_skills + 1 ))
    done
fi
green "  Skills found: $_shared_skills shared, $_local_skills local"

# ── 4. Symlink ~/.claude/agents/ ─────────────────────────────────────────────
echo
green "[4/13] Agents directory"
mkdir -p "$DOTFILES/agents"
if [[ "${ADOPT_SKIP_AGENTS:-false}" == true ]]; then
    yellow "  Skipping ~/.claude/agents link (--adopt skip)"
else
    ensure_symlink "$DOTFILES/agents" "$CLAUDE_DIR/agents" "~/.claude/agents"
fi

_agents=0
for f in "$DOTFILES/agents/"*.md; do
    [[ -f "$f" ]] && _agents=$(( _agents + 1 ))
done
green "  Agents found: $_agents"

# ── 5. Symlink ~/.claude/rules/ ──────────────────────────────────────────────
echo
green "[5/13] Rules directory"
mkdir -p "$DOTFILES/rules"
if [[ "${ADOPT_SKIP_RULES:-false}" == true ]]; then
    yellow "  Skipping ~/.claude/rules link (--adopt skip)"
else
    ensure_symlink "$DOTFILES/rules" "$CLAUDE_DIR/rules" "~/.claude/rules"
fi

_rules=0
for f in "$DOTFILES/rules/"*.md; do
    [[ -f "$f" ]] && _rules=$(( _rules + 1 ))
done
green "  Rules found: $_rules"

# ── 6. Symlink ~/.claude/commands/ ────────────────────────────────────────────
echo
green "[6/13] Commands directory"
mkdir -p "$DOTFILES/commands"
ensure_symlink "$DOTFILES/commands" "$CLAUDE_DIR/commands" "~/.claude/commands"

_commands=0
for f in "$DOTFILES/commands/"*.md; do
    [[ -f "$f" ]] && _commands=$(( _commands + 1 ))
done
green "  Commands found: $_commands"

# ── 7. Claude Code hooks ──────────────────────────────────────────────────────
echo
green "[7/13] Claude Code hooks"
mkdir -p "$DOTFILES/hooks"
ensure_symlink "$DOTFILES/hooks" "$CLAUDE_DIR/hooks" "~/.claude/hooks"

_hooks=0
for f in "$DOTFILES/hooks/"*.sh; do
    [[ -f "$f" ]] && _hooks=$(( _hooks + 1 ))
done
green "  Hook scripts found: $_hooks"

# Merge hook configuration into ~/.claude/settings.json
if [[ -f "$DOTFILES/hooks/settings-hooks.json" ]]; then
    if [[ -f "$CLAUDE_DIR/settings.json" ]]; then
        if command -v jq &>/dev/null; then
            # Always merge — settings-hooks.json is source of truth for hooks
            jq -s '.[0] * .[1]' "$CLAUDE_DIR/settings.json" "$DOTFILES/hooks/settings-hooks.json" > "$CLAUDE_DIR/settings.json.tmp"
            mv "$CLAUDE_DIR/settings.json.tmp" "$CLAUDE_DIR/settings.json"
            green "  Merged hook config into ~/.claude/settings.json"
        else
            yellow "  jq not found — cannot merge hooks into settings.json"
            yellow "  Install jq and re-run, or manually merge hooks/settings-hooks.json"
        fi
    else
        cp "$DOTFILES/hooks/settings-hooks.json" "$CLAUDE_DIR/settings.json"
        green "  Created ~/.claude/settings.json with hook configuration"
    fi
fi

# ── 8. Symlink ~/.copilot/skills/ ────────────────────────────────────────────
echo
green "[8/13] Copilot skills directory"
mkdir -p "$COPILOT_DIR"
ensure_symlink "$DOTFILES/skills" "$COPILOT_DIR/skills" "~/.copilot/skills"

# ── 9. Symlink ~/.agents/skills/ ─────────────────────────────────────────────
echo
green "[9/13] Agent framework skills directory"
mkdir -p "$AGENTS_DIR"
ensure_symlink "$DOTFILES/skills" "$AGENTS_DIR/skills" "~/.agents/skills"

# ── 9.5. CLI entry points (ctrl + shft) ──────────────────────────────────────
echo
green "[9.5/13] CLI entry points (ctrl + shft)"

LOCAL_BIN="$HOME/.local/bin"
mkdir -p "$LOCAL_BIN"

# Make scripts executable
chmod +x "$DOTFILES/bin/ctrl" 2>/dev/null || true
chmod +x "$DOTFILES/shft/shft" 2>/dev/null || true

_install_cli() {
    local source="$1" target="$2" label="$3"
    local _os="${OS:-$(detect_os)}"

    if [[ ! -f "$source" ]]; then
        yellow "  $label source not found at $source — skipping"
        return
    fi

    if [[ -L "$target" ]]; then
        local _current
        _current=$(readlink "$target")
        if [[ "$_current" == "$source" ]]; then
            yellow "  $label already symlinked correctly — skipping"
            return
        fi
    fi

    if [[ "$_os" == "windows" ]]; then
        cp "$source" "$target"
        chmod +x "$target" 2>/dev/null || true
        green "  Installed $label (Windows copy)"
    else
        ln -sf "$source" "$target"
        green "  Symlinked $label"
    fi
}

_install_cli "$DOTFILES/bin/ctrl" "$LOCAL_BIN/ctrl" "ctrl → ~/.local/bin/ctrl"
_install_cli "$DOTFILES/shft/shft" "$LOCAL_BIN/shft" "shft → ~/.local/bin/shft"

# ── 10. Wire up shell ─────────────────────────────────────────────────────────
echo
green "[10/13] Shell integration"

_SHELL_SNIPPET=$(cat << 'SHELLEOF'

## BEGIN ctrlshft

# ── dotfiles/load-secrets ──
[[ -f ~/dotfiles/bin/load-secrets.sh ]] && source ~/dotfiles/bin/load-secrets.sh

# ── dotfiles/cli (ctrl + shft) ──
[[ -d "$HOME/.local/bin" ]] && [[ ":$PATH:" != *":$HOME/.local/bin:"* ]] && export PATH="$HOME/.local/bin:$PATH"

# ── dotfiles/hud ──
[[ -f ~/dotfiles/bin/write-hud-state.sh ]] && source ~/dotfiles/bin/write-hud-state.sh
[[ -x ~/dotfiles/bin/ctrlshft-claude ]] && alias claude='~/dotfiles/bin/ctrlshft-claude'

# ── dotfiles/context-detection ──
_load_context() {
    [[ -f ~/dotfiles/bin/detect-context.sh ]] \
        && source ~/dotfiles/bin/detect-context.sh > /dev/null 2>&1
    [[ -f ~/dotfiles/bin/detect-client.sh ]] \
        && source ~/dotfiles/bin/detect-client.sh > /dev/null 2>&1
}
cd() { builtin cd "$@" && _load_context; }
_load_context

## END ctrlshft
SHELLEOF
)

_wire_shell_rc() {
    local rc_file="$1"
    local rc_name="$2"

    if [[ -f "$rc_file" ]] && grep -qF "## BEGIN ctrlshft" "$rc_file"; then
        # Managed snippet exists — check if update needed
        local _existing
        _existing=$(sed -n '/^## BEGIN ctrlshft$/,/^## END ctrlshft$/p' "$rc_file")
        local _new
        _new=$(printf '%s\n' "$_SHELL_SNIPPET" | sed -n '/^## BEGIN ctrlshft$/,/^## END ctrlshft$/p')
        if [[ "$_existing" == "$_new" ]]; then
            yellow "  $rc_name shell snippet is up to date — skipping"
        else
            cp "$rc_file" "${rc_file}.bak.$(date +%s)"
            # Replace the managed block in-place
            local _tmp="${rc_file}.tmp.$$"
            awk '
                /^## BEGIN ctrlshft$/ { skip=1; next }
                /^## END ctrlshft$/   { skip=0; next }
                !skip { print }
            ' "$rc_file" > "$_tmp"
            # Re-insert the new snippet at the top
            printf '%s\n' "$_SHELL_SNIPPET" > "$rc_file"
            cat "$_tmp" >> "$rc_file"
            rm -f "$_tmp"
            green "  Updated managed shell snippet in $rc_name"
        fi
    elif [[ -f "$rc_file" ]] && grep -qF "load-secrets.sh" "$rc_file"; then
        # Legacy snippet (pre-markers) — migrate
        cp "$rc_file" "${rc_file}.bak.$(date +%s)"
        yellow "  Detected legacy shell snippet in $rc_name — migrating"
        # Remove the legacy managed block (secrets + context-detection)
        local _tmp="${rc_file}.tmp.$$"
        awk '
            # Start of legacy block
            /^# ── Secrets ──/ || /^# ── dotfiles\/load-secrets ──/ { skip=1 }
            # End of legacy block: standalone _load_context call
            skip && /^_load_context[[:space:]]*$/ { skip=0; next }
            # Also skip the end marker if present
            skip && /^# ── dotfiles\/load-secrets:end ──/ { skip=0; next }
            !skip { print }
        ' "$rc_file" > "$_tmp"
        # Prepend managed snippet, then append remaining user content
        printf '%s\n' "$_SHELL_SNIPPET" > "$rc_file"
        cat "$_tmp" >> "$rc_file"
        rm -f "$_tmp"
        green "  Migrated $rc_name to managed shell snippet with markers"
    else
        printf '%s\n' "$_SHELL_SNIPPET" >> "$rc_file"
        green "  Appended shell snippet to $rc_name"
    fi
}

BASHRC="$HOME/.bashrc"
ZSHRC="$HOME/.zshrc"

if [[ "$MINIMAL_MODE" == true ]]; then
    yellow "  Skipping shell integration (--minimal mode)"
else
    if [[ -f "$BASHRC" ]] || [[ "$(basename "$SHELL" 2>/dev/null)" == "bash" ]]; then
        _wire_shell_rc "$BASHRC" "~/.bashrc"
    fi
    if [[ -f "$ZSHRC" ]] || [[ "$(basename "$SHELL" 2>/dev/null)" == "zsh" ]]; then
        _wire_shell_rc "$ZSHRC" "~/.zshrc"
    fi
fi

# ── 11. Python venv ──────────────────────────────────────────────────────────
echo
green "[11/13] Python venv"
if [[ "$MINIMAL_MODE" == true ]]; then
    yellow "  Skipping Python venv setup (--minimal mode)"
else
    # Find a python executable (venv first, then system)
    if ! find_python; then
        yellow "  Python not found — skipping venv setup"
        yellow "  Install Python 3.10+ and re-run this script"
    elif [[ -d "$VENV_DIR" ]]; then
        # Verify venv is functional, not just present
        find_venv_python
        if [[ -n "$_venv_python" ]] && "$_venv_python" --version &>/dev/null; then
            yellow "  Venv already exists at $VENV_DIR — skipping creation"
        else
            red "  Venv directory exists but Python binary is broken: $VENV_DIR"
            red "  Fix with: rm -rf $VENV_DIR && bash ~/dotfiles/bin/bootstrap.sh"
            _fail=1
        fi
    else
        green "  Creating venv with $PYTHON..."
        "$PYTHON" -m venv "$VENV_DIR"
        green "  Venv created"
    fi

    # Install local skill packages (runs on every bootstrap, not just venv creation)
    if [[ -d "$VENV_DIR" ]]; then
        _venv_pip=""
        [[ -f "$VENV_DIR/Scripts/pip.exe" ]] && _venv_pip="$VENV_DIR/Scripts/pip.exe"
        [[ -f "$VENV_DIR/bin/pip" ]] && _venv_pip="$VENV_DIR/bin/pip"
        if [[ -n "$_venv_pip" ]] && [[ -f "$DOTFILES/skills/_local/requirements.txt" ]]; then
            "$_venv_pip" install --quiet -r "$DOTFILES/skills/_local/requirements.txt"
            green "  Installed local skill packages from skills/_local/requirements.txt"
        fi
    fi
fi

# ── 12. Supply chain attack protection ────────────────────────────────────────
echo
green "[12/13] Package manager security (supply chain protection)"
if [[ "$MINIMAL_MODE" == true ]]; then
    yellow "  Skipping supply-chain hardening (--minimal mode)"
else
    # ~/.npmrc — refuse npm packages published < 7 days ago
    if [[ -f "$HOME/.npmrc" ]] && grep -qF "min-release-age" "$HOME/.npmrc"; then
        yellow "  ~/.npmrc already has min-release-age — skipping"
    else
        echo 'min-release-age=7' >> "$HOME/.npmrc"
        green "  Added min-release-age=7 to ~/.npmrc"
    fi

    # ~/.config/uv/uv.toml — refuse uv/Python packages published < 7 days ago
    # uv requires an ISO date (YYYY-MM-DD), so we compute 7 days ago at bootstrap time.
    # Re-run bootstrap periodically to advance the date.
    UV_CONFIG_DIR="$HOME/.config/uv"
    UV_CONFIG="$UV_CONFIG_DIR/uv.toml"
    UV_DATE=$(date -d "-7 days" +%Y-%m-%d 2>/dev/null || date -v-7d +%Y-%m-%d 2>/dev/null || echo "")
    if [[ -z "$UV_DATE" ]]; then
        yellow "  Could not compute date — skipping uv exclude-newer"
    elif [[ -f "$UV_CONFIG" ]] && grep -qF "exclude-newer" "$UV_CONFIG"; then
        # Update existing date to current run (atomic write — portable across GNU/BSD sed)
        sed "s/exclude-newer = \".*\"/exclude-newer = \"$UV_DATE\"/" "$UV_CONFIG" > "$UV_CONFIG.tmp" && mv "$UV_CONFIG.tmp" "$UV_CONFIG"
        green "  Updated exclude-newer = \"$UV_DATE\" in ~/.config/uv/uv.toml"
    else
        mkdir -p "$UV_CONFIG_DIR"
        echo "exclude-newer = \"$UV_DATE\"" >> "$UV_CONFIG"
        green "  Added exclude-newer = \"$UV_DATE\" to ~/.config/uv/uv.toml"
    fi
fi

# ── 13. Validation ────────────────────────────────────────────────────────────
echo
green "[13/13] Validating setup"

# Delegate to validate-env.sh — single source of truth for all validation checks.
# validate-env.sh sets _fail and _warn internally and prints results.
# Save bootstrap's _fail before sourcing — validate-env also uses _fail.
_bootstrap_fail=$_fail
if [[ "$MINIMAL_MODE" == true ]]; then
    yellow "  Minimal mode: skipping validate-env (expects secrets/shell/venv checks)"
    if bash "$HOME/dotfiles/bin/validate-symlinks.sh"; then
        :
    else
        _bootstrap_fail=1
    fi
else
    _fail=0
    source "$(dirname "${BASH_SOURCE[0]}")/validate-env.sh"
    (( _fail )) && _bootstrap_fail=1
fi
_fail=$_bootstrap_fail

echo ""
echo "Next steps:"
_step=1
if [[ ! -s "$SECRETS_DIR/.env.agent" ]] || grep -q "^GITHUB_USERNAME=$" "$SECRETS_DIR/.env.agent" 2>/dev/null; then
    yellow "  $_step. Fill in config:    \$EDITOR ~/dotfiles/secrets/.env.agent"
    ((_step++))
    yellow "  $_step. Fill in secrets:  \$EDITOR ~/dotfiles/secrets/.env.secrets"
    ((_step++))
fi
echo "  $_step. Reload shell:    source ~/.bashrc"
((_step++))
if [[ -d "$HOME/.vscode-server" ]]; then
    echo "  $_step. Sync VS Code settings on your LOCAL machine (not this VPS)"
else
    echo "  $_step. Merge VS Code settings:  bash ~/dotfiles/bin/sync-settings.sh"
fi
((_step++))
echo "  $_step. Verify:          echo \$GITHUB_USERNAME"
((_step++))
echo "  $_step. Try:             ctrl help, shft help"
echo ""
printf '\033[32mctrl+shft ready — type \033[33mctrl help\033[32m to explore\033[0m\n'

exit $_fail
